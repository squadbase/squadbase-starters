export type TranslationKey = keyof typeof translations;

/**
 * Get translated text
 */
export const t = (key: TranslationKey): string => {
  const language = getLanguage();
  const translation = translations[key] as Record<string, string>;
  return translation[language] || translation['en'] || key;
};

/**
 * Currency formatting
 */
export const formatCurrency = (amount: number): string => {
  const currency = getCurrency();
  const language = getLanguage();

  // Map languages to their corresponding locales
  const localeMap: Record<Language, string> = {
    ja: 'ja-JP',
    en: 'en-US',
    es: 'es-ES',
    fr: 'fr-FR',
    zh: 'zh-CN',
    ko: 'ko-KR',
  };

  const locale = localeMap[language] || 'en-US';

  // Currencies that don't use decimal places
  const noDecimalCurrencies = ['JPY', 'KRW', 'CNY'];

  return new Intl.NumberFormat(locale, {
    style: 'currency',
    currency: currency,
    minimumFractionDigits: noDecimalCurrencies.includes(currency) ? 0 : 2,
  }).format(amount);
};

/**
 * Number formatting (comma-separated)
 */
export const formatNumber = (value: number): string => {
  const language = getLanguage();
  
  // Map languages to their corresponding locales
  const localeMap: Record<Language, string> = {
    ja: 'ja-JP',
    en: 'en-US',
    es: 'es-ES',
    fr: 'fr-FR',
    zh: 'zh-CN',
    ko: 'ko-KR',
  };

  const locale = localeMap[language] || 'en-US';

  return new Intl.NumberFormat(locale).format(value);
};

/**
 * Date formatting - yyyy/MM/dd format
 */
export const formatDate = (date: string | Date): string => {
  const dateObj = typeof date === 'string' ? new Date(date) : date;

  const year = dateObj.getFullYear();
  const month = String(dateObj.getMonth() + 1).padStart(2, '0');
  const day = String(dateObj.getDate()).padStart(2, '0');

  return `${year}/${month}/${day}`;
};
