type Language = 'ja' | 'en' | 'es' | 'fr' | 'zh' | 'ko';
type Currency = 'JPY' | 'USD' | 'EUR' | 'CNY' | 'KRW';

// Server-side only configuration retrieval (use only in API routes)
export const getServerLanguage = (): Language => {
  const lang = process.env.LANGUAGE || 'en';
  const langCode = lang.toLowerCase();
  if (['ja', 'en', 'es', 'fr', 'zh', 'ko'].includes(langCode)) {
    return langCode as Language;
  }
  return 'en';
};

export const getServerCurrency = (): Currency => {
  const currency = process.env.CURRENCY || 'usd';
  const currencyCode = currency.toUpperCase();
  if (['JPY', 'USD', 'EUR', 'CNY', 'KRW'].includes(currencyCode)) {
    return currencyCode as Currency;
  }
  return 'USD';
};

